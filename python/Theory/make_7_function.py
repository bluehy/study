# minus
def i_minus(a,b):
   return int(a) - int(b)
   
# times
def i_times(a,b):
   return int(a) * int(b)

# division
def i_division(a,b):
   return int(a) / int(b)

# negation
def i_negation(a):
   return -int(a)

# power
def i_power(a,b):
   return pow(int(a), int(b))

# remainder
def i_remainder(a,b):
   return int(a) % int(b)

